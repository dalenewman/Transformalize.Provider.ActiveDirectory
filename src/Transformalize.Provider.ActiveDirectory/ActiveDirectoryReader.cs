        private readonly InputContext _input;
        private readonly IRowFactory _rowFactory;
        private readonly List<FieldMatch> _validFields = new List<FieldMatch>();
        private readonly string[] _validFieldNames;

        public ActiveDirectoryReader(InputContext input, IRowFactory rowFactory) {
            _input = input;
            _rowFactory = rowFactory;
            var lookUp = GetActiveDirectoryFields();

            foreach (var field in input.Entity.GetAllFields().Where(f => f.Input)) {
                if (lookUp.ContainsKey(field.Name)) {
                    _validFields.Add(new FieldMatch { Field = field, ActiveDirectoryField = lookUp[field.Name] });
                } else {
                    _input.Warn($"The active directory provider does not recognize the field {field.Name}.");
                }
            }

            _validFieldNames = _validFields.Select(f => f.ActiveDirectoryField.AttrLdapName).ToArray();
        }

        public IEnumerable<IRow> Read() {
            var count = 0;
            var entry = new DirectoryEntry();
            var filter = _input.Entity.Filter.FirstOrDefault(f => f.Expression != string.Empty);

            using (var search = new DirectorySearcher(entry)) {

                if (filter != null) {
                    search.Filter = filter.Expression;
                }
                search.PageSize = _input.Entity.Size > 0 ? _input.Entity.Size : 1000;
                search.PropertiesToLoad.AddRange(_validFieldNames);


                var start = _input.Connection.Start;
                SearchResultCollection results;

                if (_input.Entity.IsPageRequest()) {
                    start += (_input.Entity.Page * _input.Entity.Size) - _input.Entity.Size;
                    var end = start + _input.Entity.Size;

                    try {
                        var test = search.FindOne();
                        if (test != null) {
                            results = search.FindAll();
                        } else {
                            yield break;
                        }
                    } catch (ArgumentException e) {
                        _input.Error(e.Message);
                        yield break;
                    }
                    foreach (SearchResult result in results) {
                        ++count;
                        if (count >= start && count <= end) {
                            var item = result.GetDirectoryEntry();
                            item.RefreshCache(_validFieldNames);
                            yield return ConvertToRow(item);
                        }
                        if (count > end) {
                            break;
                        }
                    }
                } else {
                    try {
                        var test = search.FindOne();
                        if (test != null) {
                            results = search.FindAll();
                        } else {
                            yield break;
                        }
                    } catch (ArgumentException e) {
                        _input.Error(e.Message);
                        yield break;
                    }



                    foreach (SearchResult result in results) {
                        var item = result.GetDirectoryEntry();
                        item.RefreshCache(_validFieldNames);
                        yield return ConvertToRow(item);
                    }
                }


            }

