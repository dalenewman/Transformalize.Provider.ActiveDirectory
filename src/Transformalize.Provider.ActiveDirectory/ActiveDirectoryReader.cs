        private IRow ConvertToRow(Principal result) {
            var row = _rowFactory.Create();
            if (result.GetUnderlyingObject() is DirectoryEntry de) {
                foreach (var fieldMatch in _validFields) {
                    var value = de.Properties[fieldMatch.ActiveDirectoryField.AttrLdapName].Value;

                    if (value != null) {
                        if (fieldMatch.ActiveDirectoryField.MultiValue) {
                            if (value is object[] oa) {
                                row[fieldMatch.Field] = string.Join("|", oa);
                            }
                        } else {
                            row[fieldMatch.Field] = fieldMatch.Field.Convert(value);
                        }
                    }
                }
            }
            return row;
        }

        private IRow ConvertToRow(DirectoryEntry result) {
            var row = _rowFactory.Create();

            foreach (var fieldMatch in _validFields) {
                object value = null;

                value = result.Properties[fieldMatch.ActiveDirectoryField.AttrLdapName].Value;

                if (value == null)
                    continue;

                if (fieldMatch.ActiveDirectoryField.MultiValue) {
                    if (value is object[] oa) {
                        row[fieldMatch.Field] = string.Join(_input.Connection.Delimiter, oa);
                    }
                } else {
                    row[fieldMatch.Field] = fieldMatch.Field.Convert(value);
                }
            }

            return row;
        }

